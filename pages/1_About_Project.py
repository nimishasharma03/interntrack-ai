import streamlit as st

st.title("📘 About InternTrack AI")

st.markdown("""
### Overview

InternTrack AI is an internship application management platform built using Streamlit.

### Features

- Application Tracking
- Follow-Up Reminders
- Interview Preparation
- Resume Match Analysis
- Application Analytics
- Career Insights

### Tech Stack

- Python
- Streamlit
- Pandas
- Plotly
- Scikit-learn

### Purpose

Students often struggle to manage dozens of internship applications across multiple platforms.

InternTrack AI centralizes the entire application process into one dashboard and helps students stay organized throughout their internship search.
""")